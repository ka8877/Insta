package com.insta.profile;

public class ProfileVO {

	private int mseq;
	private String openGubun; 
	private String gender;
	private String website;
	private String intro;
	private String myPicturePath;
	private String myPictureName;
	
	public int getMseq() {
		return mseq;
	}
	public void setMseq(int mseq) {
		this.mseq = mseq;
	}
	public String getOpenGubun() {
		return openGubun;
	}
	public void setOpenGubun(String openGubun) {
		this.openGubun = openGubun;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getWebsite() {
		return website;
	}
	public void setWebsite(String website) {
		this.website = website;
	}
	public String getIntro() {
		return intro;
	}
	public void setIntro(String intro) {
		this.intro = intro;
	}
	public String getMyPicturePath() {
		return myPicturePath;
	}
	public void setMyPicturePath(String myPicturePath) {
		this.myPicturePath = myPicturePath;
	}
	public String getMyPictureName() {
		return myPictureName;
	}
	public void setMyPictureName(String myPictureName) {
		this.myPictureName = myPictureName;
	}
	
	
	
}
